package com.company;

public class metodos {
    public int sumar( int n1, int n2){
        int resultados ;
        resultados = n1+ n2;
        return resultados;
    }
    public int restar( int n1, int n2){
        int resultados ;
        resultados = n1- n2;
        return resultados;
    }
    public int multiplicar( int n1, int n2){
        int resultados ;
        resultados = n1* n2;
        return resultados;
    }
    public int dividir( int n1, int n2){
        int resultados ;
        resultados = n1/n2;
        return resultados;
    }

}
